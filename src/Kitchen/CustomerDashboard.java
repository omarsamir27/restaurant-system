    private void ShowOrderbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ShowOrderbtnActionPerformed
        // TODO add your handling code here:
        CustomerTabs.remove(0);
        CustomerTabs.remove(0);
        CustomerTabs.add("Order Details",OrderDetails);
        order.setPriceWithoutTax(0);
        order.setOrderPrice(0);
        boolean flag=false;
        for (int i=0 ; i<toggles.size();i++){
            if(toggles.get(i).isSelected()){
                if(dishlist.size()==0)
                    JOptionPane.showMessageDialog(null,"Please Make An Order");
                else{
                   
                    order.setDishes(dishlist);
                    order.calcOrderPrice();
                    order.calcTotalOrderPrice();
                      String text1=("Customer Name  :  " + customer.getName() + "\n" + 
                      "Table Number  :  " + ButtonMap.get(toggles.get(i)).getTableNum() + "\n");
                      String text2 = "";
                      table.setTableNum(ButtonMap.get(toggles.get(i)).getTableNum());
                      table.setSeatsNum(ButtonMap.get(toggles.get(i)).getSeatsNum());
                      table.setSmoking(ButtonMap.get(toggles.get(i)).isSmoking());
                      table.setAvailable(false);
                      order.setTableNum(ButtonMap.get(toggles.get(i)).getTableNum());
                      for(int j=0;j<dishlist.size();j++){
                      text2 = ( text2 + dishlist.get(j).getDishName() + "  " + 
                      dishlist.get(j).getDishPrice() + "\n");
                      }
                      String text3 =("Price Without Taxes  :  " + order.getPriceWithoutTax() + "\n" +
                      "Price after Taxes  :  " + order.getOrderPrice());
                      Details.setText(text1 + text2 + text3);
                }
                flag=true;
                break;
            }
        }
        if(!flag)
            JOptionPane.showMessageDialog(null,"Please Select A Table");
            
    }//GEN-LAST:event_ShowOrderbtnActionPerformed

