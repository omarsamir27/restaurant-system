    private void ShowbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ShowbtnActionPerformed
        // TODO add your handling code here:
        PlaceOrderbtn.setVisible(true);
        for(int i=0 ; i<toggles.size(); i++){
            toggles.get(i).setVisible(false);
        }
        for (Map.Entry<JToggleButton, Table> entry : ButtonMap.entrySet()) {
            entry.getKey().setText("<html><center>" + "Number " + entry.getValue().getTableNum() + "<br>" + "Seats " 
                    +entry.getValue().getSeatsNum() + "</html>" );
            if (jRadioButton1.isSelected()){
                if((int)jSpinner1.getValue() <= entry.getValue().getSeatsNum() && entry.getValue().isSmoking()==true && entry.getValue().isAvailable() == true)
                    entry.getKey().setVisible(true);
            }
            else if (jRadioButton2.isSelected()){
                if((int)jSpinner1.getValue() <= entry.getValue().getSeatsNum() && entry.getValue().isSmoking() == false && entry.getValue().isAvailable() == true)
                    entry.getKey().setVisible(true);
            }
            else
                JOptionPane.showMessageDialog(null,"Sorry , No Available Tables By These Properties For Now");
        }
    }//GEN-LAST:event_ShowbtnActionPerformed

